package com.tes.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;
import lombok.Data;

@Entity
@Table(name = "filter_use_names")
@Data
public class FilterUseNames
{
	@Id
	@GeneratedValue
	@Column(name = "id")
	private int id;

	@Column(name = "filter_use_name")
	private String FilterUseName;
}
